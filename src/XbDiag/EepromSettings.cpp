{
    EepRecalcChecksums(buf);
}

void EepromSettings_GetUtcTzBlock(BYTE out[44])
{
    // Find first UTC+0 entry (London/Dublin/Lisbon) and copy its 44-byte raw block
    int tzIdx = 0;
    for (int i = 0; i < TZ_COUNT; ++i)
        if (TzRawBias(s_tzTable[i]) == 0) { tzIdx = i; break; }
    for (int i = 0; i < 44; ++i)
        out[i] = s_tzTable[tzIdx].raw[i];
}